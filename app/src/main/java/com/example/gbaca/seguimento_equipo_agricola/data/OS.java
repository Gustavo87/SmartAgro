public class OS {

    private String Codigo_OS;
    private String Plantio;
    private String Plantio_N;
    private String Actividad;
    private String Actividad_N;
    private String Tipo_Actividad;
    private String Inicio_Actividad;
    private String Fin_Actividad;

    public OS() {}

    public String getPlantio_N() {
        return Plantio_N;
    }

    public void setPlantio_N(String plantio_N) {
        Plantio_N = plantio_N;
    }

    public String getActividad_N() {
        return Actividad_N;
    }

    public void setActividad_N(String actividad_N) {
        Actividad_N = actividad_N;
    }

    public String getTipo_Actividad() {
        return Tipo_Actividad;
    }

    public void setTipo_Actividad(String tipo_Actividad) {
        Tipo_Actividad = tipo_Actividad;
    }

    public String getCodigo_OS() {
        return Codigo_OS;
    }

    public void setCodigo_OS(String codigo_OS) {
        Codigo_OS = codigo_OS;
    }

    public String getPlantio() {
        return Plantio;
    }

    public void setPlantio(String plantio) {
        Plantio = plantio;
    }

    public String getActividad() {
        return Actividad;
    }

    public void setActividad(String actividad) {
        Actividad = actividad;
    }

    public String getInicio_Actividad() {
        return Inicio_Actividad;
    }

    public void setInicio_Actividad(String inicio_Actividad) {
        Inicio_Actividad = inicio_Actividad;
    }

    public String getFin_Actividad() {
        return Fin_Actividad;
    }

    public void setFin_Actividad(String fin_Actividad) {
        Fin_Actividad = fin_Actividad;
    }
}
